#include "TMath.h"

double etaVal(int ieta) {

  double etavl;

  if (ieta <= -24){
      etavl = .1695*ieta + 1.9931;
  }
  else if (ieta <= -1){
      etavl = .0875*ieta + .0489;
  }
  else if (ieta < 24){
      etavl = .0875*ieta - .0489;
  }
  else {
      etavl = .1695*ieta - 1.9931;
  }
  return etavl;

}

double phiVal(int iphi) {

  double phiBins=72.;

  double phivl;
  phivl=double(iphi)*(2.*TMath::Pi()/phiBins);

  if (iphi > 36)
    phivl -= 2.*TMath::Pi();

  return phivl;

}

